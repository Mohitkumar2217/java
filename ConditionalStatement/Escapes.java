public class Escapes {
    public static void main(String[] args) {
        System.out.println("\"Hello Mohit\"");
        System.out.println("\'Hello Mohit\'");
        System.out.println("\\Hello Mohit\\");
        System.out.println("Hello\tMohit");
        System.out.println("Hello \bMohit\n");
        System.out.println("\bHello\tMohit\n");
    }
}
