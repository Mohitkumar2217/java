public class shotHand {
    public static void main(String[] args) {
        int Val;
        Val = 4;
        System.out.println(Val);
        Val += 5;
        System.out.println(Val);
        Val -= 5;
        System.out.println(Val);
        Val *= 5;
        System.out.println(Val);
        Val /= 5;
        System.out.println(Val);
        Val++;
        System.out.println(Val);
        ++Val;
        System.out.println(Val);
        Val--;
        System.out.println(Val);
        --Val;
        System.out.println(Val);
        int v;
        v = ++Val;
        System.out.println(v);
        v = Val++;
        System.out.println(v);
        System.out.println(Val++);
        System.out.println(++Val);
        System.out.println(Val--);
        System.out.println(Val);
        System.out.println(--Val);
    }
}
