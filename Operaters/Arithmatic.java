public class Arithmatic {
    public static void main(String[] args) {
        int num1 = 16, num2 = 5;
        System.out.println("Sum of numbers " + (num1 + num2));
        System.out.println("Sub of numbers " + (num1 - num2));
        System.out.println("Mul of numbers " + (num1 * num2));
        System.out.println("Div of numbers " + (num1 / num2));
        System.out.println("Mod of numbers " + (num1 % num2));
        double n1 = 15, n2 = 7;
        System.out.println("Sum of numbers " + (n1 + n2));
        System.out.println("Sub of numbers " + (n1 - n2));
        System.out.println("Mul of numbers " + (n1 * n2));
        System.out.println("Div of numbers " + (n1 / n2));
    }
}
